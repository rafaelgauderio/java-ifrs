
public class Animal {
	
	private double peso;
	private String nome;
	private String raca;
	
	public Animal () {
		
	}

	public Animal(double peso, String nome, String raca) {
		this.peso = peso;
		this.nome = nome;
		this.raca = raca;
	}

	public double getPeso() {
		return peso;
	}

	public void setPeso(double peso) {
		this.peso = peso;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getRaca() {
		return raca;
	}

	public void setRaca(String raca) {
		this.raca = raca;
	}
	
		
	
	

}
